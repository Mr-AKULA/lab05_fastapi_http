# model/classifier.py
"""
Архитектура сверточной нейронной сети (CNN) для классификации изображений CIFAR-10.

Набор данных CIFAR-10:
  - 60 000 цветных изображений 32×32 пикселя
  - 10 классов: airplane, automobile, bird, cat, deer,
                dog, frog, horse, ship, truck
  - 50 000 обучающих / 10 000 тестовых

Архитектура CIFAR10CNN состоит из трёх свёрточных блоков
с нарастающим числом фильтров (32 → 64 → 128) и убывающим
пространственным разрешением (32 → 16 → 8 → 4), после чего
следует полносвязная «голова» классификатора.
"""

import torch
import torch.nn as nn
import torch.nn.functional as F

# Метки классов CIFAR-10 в порядке индексов 0..9
CIFAR10_CLASSES = (
    "airplane",    # 0 — самолёт
    "automobile",  # 1 — автомобиль
    "bird",        # 2 — птица
    "cat",         # 3 — кошка
    "deer",        # 4 — олень
    "dog",         # 5 — собака
    "frog",        # 6 — лягушка
    "horse",       # 7 — лошадь
    "ship",        # 8 — корабль
    "truck",       # 9 — грузовик
)


class CIFAR10CNN(nn.Module):
    """
    Сверточная нейронная сеть для классификации изображений CIFAR-10.

    Структура сети:
    ┌─────────────────────────────────────────────────────┐
    │ Блок 1: Conv(3→32) → BN → ReLU                     │
    │         Conv(32→32) → BN → ReLU → MaxPool → Dropout │  32×32 → 16×16
    │ Блок 2: Conv(32→64) → BN → ReLU                    │
    │         Conv(64→64) → BN → ReLU → MaxPool → Dropout │  16×16 → 8×8
    │ Блок 3: Conv(64→128) → BN → ReLU → MaxPool → Dropout│  8×8  → 4×4
    │ Flatten: 128 × 4 × 4 = 2048                         │
    │ FC: 2048 → 256 → ReLU → Dropout                     │
    │ FC: 256  → 10  (логиты, без softmax)                 │
    └─────────────────────────────────────────────────────┘

    Итого параметров: ~667 000.

    Почему такая архитектура:
      - Увеличение числа фильтров (32→64→128) позволяет сети
        обнаруживать всё более сложные признаки в глубоких слоях.
      - BatchNorm ускоряет обучение и стабилизирует градиенты.
      - MaxPool уменьшает пространственные размеры, сокращая число
        параметров и добавляя трансляционную инвариантность.
      - Dropout(0.25) снижает переобучение на обучающей выборке.
    """

    def __init__(self, num_classes: int = 10) -> None:
        """
        Инициализация слоёв сети.

        Args:
            num_classes: количество классов на выходе (по умолчанию 10 для CIFAR-10).
        """
        super().__init__()

        # ── Блок 1 ────────────────────────────────────────────────────────────
        # Входной тензор: (B, 3, 32, 32) — B изображений, 3 канала RGB, 32×32
        # После блока:    (B, 32, 16, 16)
        self.conv1 = nn.Conv2d(3, 32, kernel_size=3, padding=1)
        # padding=1 сохраняет пространственный размер: (32+2-3)/1+1 = 32
        self.bn1 = nn.BatchNorm2d(32)
        self.conv2 = nn.Conv2d(32, 32, kernel_size=3, padding=1)
        self.bn2 = nn.BatchNorm2d(32)

        # ── Блок 2 ────────────────────────────────────────────────────────────
        # Входной тензор: (B, 32, 16, 16)
        # После блока:    (B, 64, 8, 8)
        self.conv3 = nn.Conv2d(32, 64, kernel_size=3, padding=1)
        self.bn3 = nn.BatchNorm2d(64)
        self.conv4 = nn.Conv2d(64, 64, kernel_size=3, padding=1)
        self.bn4 = nn.BatchNorm2d(64)

        # ── Блок 3 ────────────────────────────────────────────────────────────
        # Входной тензор: (B, 64, 8, 8)
        # После блока:    (B, 128, 4, 4)
        self.conv5 = nn.Conv2d(64, 128, kernel_size=3, padding=1)
        self.bn5 = nn.BatchNorm2d(128)

        # ── Общие слои ────────────────────────────────────────────────────────
        # MaxPool(2,2): делит каждое пространственное измерение на 2
        self.pool = nn.MaxPool2d(2, 2)
        # Dropout(0.25): случайно обнуляет 25% нейронов во время обучения
        self.dropout = nn.Dropout(0.25)

        # ── Полносвязная «голова» ─────────────────────────────────────────────
        # Вход: вектор 128 * 4 * 4 = 2048 (после Flatten)
        self.fc1 = nn.Linear(128 * 4 * 4, 256)
        # Выход: логиты для каждого из num_classes классов
        self.fc2 = nn.Linear(256, num_classes)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Прямой проход (forward pass) через сеть.

        Паттерн каждого блока: Conv → BN → ReLU (→ повтор) → MaxPool → Dropout.

        Args:
            x: входной тензор формы (B, 3, 32, 32).

        Returns:
            Тензор логитов формы (B, num_classes).
            Softmax НЕ применяется — CrossEntropyLoss ожидает «сырые» логиты.
        """
        # ── Блок 1: (B,3,32,32) → (B,32,16,16) ──────────────────────────────
        x = F.relu(self.bn1(self.conv1(x)))  # свёртка + нормализация + активация
        x = F.relu(self.bn2(self.conv2(x)))
        x = self.pool(x)      # 32×32 → 16×16
        x = self.dropout(x)   # регуляризация

        # ── Блок 2: (B,32,16,16) → (B,64,8,8) ───────────────────────────────
        x = F.relu(self.bn3(self.conv3(x)))
        x = F.relu(self.bn4(self.conv4(x)))
        x = self.pool(x)      # 16×16 → 8×8
        x = self.dropout(x)

        # ── Блок 3: (B,64,8,8) → (B,128,4,4) ────────────────────────────────
        x = F.relu(self.bn5(self.conv5(x)))
        x = self.pool(x)      # 8×8 → 4×4
        x = self.dropout(x)

        # ── Полносвязная часть ────────────────────────────────────────────────
        x = torch.flatten(x, start_dim=1)  # (B,128,4,4) → (B,2048)
        x = F.relu(self.fc1(x))            # (B,2048) → (B,256)
        x = self.dropout(x)
        x = self.fc2(x)                    # (B,256) → (B,num_classes)
        return x  # логиты (без softmax): CrossEntropyLoss сам применит log-softmax
